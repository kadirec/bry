<h4>1. Veri Sorumlusu</h4>
<p>İşbu aydınlatma metni, 6698 sayılı Kişisel Verilerin Korunması Kanunu ("KVKK") uyarınca, <strong>Bilinçli Ritmik Yaşam (BRY)</strong> tarafından veri sorumlusu sıfatıyla; iletişim formu aracılığıyla tarafınızca paylaşılan kişisel verilerin işlenmesine ilişkin olarak sizleri bilgilendirmek amacıyla hazırlanmıştır.</p>

<h4>2. İşlenen Kişisel Veriler</h4>
<p>Form üzerinden tarafımızla paylaşmanız halinde; <strong>ad-soyad, kurum adı, e-posta adresi, telefon numarası ve ilgilendiğiniz program bilgisi</strong> tarafımızca işlenmektedir.</p>

<h4>3. Kişisel Verilerin İşlenme Amacı</h4>
<p>Paylaştığınız kişisel veriler;</p>
<ul>
  <li>Talep ettiğiniz kurumsal teklif ve bilgilendirme süreçlerinin yürütülmesi,</li>
  <li>Sizinle ön görüşme yapılması ve uygun programın belirlenmesi,</li>
  <li>Eğitim ve seminer süreçlerinin planlanması,</li>
  <li>Müşteri ilişkileri yönetimi ve kayıt altına alma,</li>
  <li>Yasal yükümlülüklerin yerine getirilmesi</li>
</ul>
<p>amaçlarıyla, KVKK madde 5/2 (sözleşmenin kurulması veya ifası ile ilgili olarak gerekli olması) ve sizin açık rızanız hukuki sebeplerine dayanılarak işlenmektedir.</p>

<h4>4. Kişisel Verilerin Aktarılması</h4>
<p>Kişisel verileriniz; yalnızca yukarıda belirtilen amaçlarla sınırlı olarak ve yasal düzenlemelerin gerektirdiği ölçüde, hizmet aldığımız iş ortaklarımıza ve yetkili kamu kurum/kuruluşlarına aktarılabilir. Verileriniz, ticari amaçla üçüncü kişilere satılmaz veya devredilmez.</p>

<h4>5. Kişisel Veri Toplama Yöntemi ve Hukuki Sebebi</h4>
<p>Kişisel verileriniz; web sitemizdeki iletişim formu aracılığıyla elektronik ortamda, sizin tarafınızdan açık rıza ile iletilmek suretiyle toplanmaktadır.</p>

<h4>6. Saklama Süresi</h4>
<p>Kişisel verileriniz, talebinizin sonuçlandırılması ve ilgili yasal saklama süreleri boyunca muhafaza edilecek; süre sonunda KVKK'nın 7. maddesine uygun olarak silinecek, yok edilecek veya anonim hale getirilecektir.</p>

<h4>7. KVKK Madde 11 Kapsamındaki Haklarınız</h4>
<p>KVKK'nın 11. maddesi uyarınca; kişisel verilerinizin işlenip işlenmediğini öğrenme, işlenmişse buna ilişkin bilgi talep etme, işlenme amacını ve amacına uygun kullanılıp kullanılmadığını öğrenme, eksik veya yanlış işlenmişse düzeltilmesini isteme, KVKK'nın öngördüğü şartlar çerçevesinde silinmesini veya yok edilmesini isteme, işleme yapılan üçüncü kişilere bildirilmesini isteme, otomatik sistemler ile analiz edilmesi suretiyle aleyhinize bir sonucun ortaya çıkmasına itiraz etme ve kanuna aykırı işleme nedeniyle uğradığınız zararın giderilmesini talep etme haklarına sahipsiniz.</p>

